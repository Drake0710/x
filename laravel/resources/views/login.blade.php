	<script>
	
		function doBounce(element, times, distance, speed) {
			for(i = 0; i < times; i++) {
				element.animate({marginTop: '-='+distance},speed)
					.animate({marginTop: '+='+distance},speed);
			}        
		}
		
		jQuery('#login-form-submit').click(function(){
			//bouncing for awhile...
			doBounce(jQuery('#login-form'), 3, '10px', 100);
			jQuery(this).prop('disabled',true);
			console.log('tes');
			jQuery(this).html('<span class="loading"><i class="fa fa-refresh"></i> Loading.....</span>');
			var lanjut=true;
			if(jQuery('#login-form-username').val()==''){
				lanjut=false;
			}
			if(jQuery('#login-form-password').val()==''){
				lanjut=false;
			}
			if(lanjut==true){
				var url="auth";
				var data=jQuery('#login-form').serialize();
				jQuery.ajax({
					url:url,
					data:data,
					method:'POST',
					dataType:'JSON',
					success:function(result){
						if(result.error==false){
							alertify.log('Login berhasil!');
							jQuery('#login-form-submit').html('<i class="fa fa-lock"></i> Login');
							jQuery('#login-form-submit').prop('disabled', false);
							window.location.href = result.message;
						}
						else{
							alertify.log(result.message);
							jQuery('#login-form-submit').html('<i class="fa fa-lock"></i> Login');
							jQuery('#login-form-submit').prop('disabled', false);
						}
					},
					error:function(result){
						alertify.log(result.responseJSON.message);
						jQuery('#login-form-submit').html('<i class="fa fa-lock"></i> Login');
						jQuery('#login-form-submit').prop('disabled', false);
					}
				});
				
			}
			else{
				alertify.log('Kolom username/password tidak dapat dikosongkan!');
				jQuery('#login-form-submit').html('<i class="fa fa-lock"></i> Login');
				jQuery('#login-form-submit').prop('disabled', false);
			}
		});
	</script>
	
